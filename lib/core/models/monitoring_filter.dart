/// - [sent]      : الطلاب الذين رفعوا تقاريرهم فعلاً.
/// - [remaining] : الطلاب المفترض رفع تقاريرهم ولم يفعلوا بعد.
/// - [all]       : جميع الطلاب دون تصفية بحسب حالة التقرير.
enum MonitoringFilter { expected, sent, remaining, all }
